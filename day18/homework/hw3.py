# სხვა შემთხვევაში დაბეჭდე --> "რიცხვი უარყოფითია"



number = int(input("შეიყვანეთ რიცხვი: "))
if number > 0:
    if number % 2 == 0:
        print("დადებითი ლუწი")
    else:
        print("დადებითი კენტი")
else:
    print("რიცხვი უარყოფითია")